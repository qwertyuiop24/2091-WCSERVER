//add
function add(a, b) {
  return a + b;
}

//subtract
function subtract(a, b) {
  return a - b;
}

//multiply
function multiply(a, b) {
  return a * b;
}

function multi_three(a,b,c){
  return a * b * c;
}

//divide
function divide(a, b) {
  return a / b;
}

module.exports = { add, subtract, multiply, multi_three, divide };